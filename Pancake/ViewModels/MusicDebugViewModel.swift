// MARK: - Debug Errors
enum DebugError: LocalizedError {
    case chatGPTNotConfigured
    case noLocalMusic

    var errorDescription: String? {
        switch self {
        case .chatGPTNotConfigured:
            return "ChatGPT/AI service is not configured. Add your API key in settings."
        case .noLocalMusic:
            return "No local Apple Music library content found on this device."
        }
    }
}

