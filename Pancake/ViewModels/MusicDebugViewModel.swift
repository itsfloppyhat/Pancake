    func testMusicSuggestion() async {
        guard isChatGPTConfigured else {
            error = DebugError.chatGPTNotConfigured
            return
        }
        
        isTesting = true
        error = nil // Clear any previous errors
        
        defer { isTesting = false }
        
        do {
            print("🧪 Starting ChatGPT test...")
            
            // Create a test context
            let testContext = MusicContext(
                currentHeartRate: 150,
                targetHeartRate: 160,
                currentIntensity: .medium,
                timeRemainingInSegment: 300,
                currentSongEndingIn: 30,
                userPreferences: profileManager.userProfile.musicPreferences,
                recentSongs: [],
                currentDistance: 1000.0, // 1km for testing
                currentPace: 3.0, // 3 m/s for testing
                isActive: true
            )
            
            print("🧪 Test context created, calling ChatGPT...")
            
            let suggestion = try await withTimeout(seconds: 30) {
                try await self.chatGPTService.generateMusicSuggestion(
                    context: testContext,
                    userPreferences: self.profileManager.userProfile.musicPreferences
                )
            }
            
            print("✅ ChatGPT test successful: \(suggestion.songTitle) by \(suggestion.artist)")
            testSuggestion = suggestion
            
        } catch {
            print("❌ ChatGPT test failed: \(error)")
            self.error = error
        }
    }
    
    func generateMadlibSuggestion() async {
        guard isChatGPTConfigured else {
            error = DebugError.chatGPTNotConfigured
            return
