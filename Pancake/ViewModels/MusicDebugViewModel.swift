            // Calculate target heart rate based on intensity
            let targetHeartRate = calculateTargetHeartRate(for: selectedIntensity)
            
            // Calculate pace from distance and time
            let pace = currentTime > 0 ? (currentDistance * 1000) / (currentTime * 60) : 0 // m/s
            
            // Calculate time remaining in segment based on workout phase
            let timeRemainingInSegment = calculateTimeRemainingInSegment()
            
            // Create context based on madlib parameters
            let madlibContext = MusicContext(
                currentHeartRate: Int(currentHeartRate),
                targetHeartRate: targetHeartRate,
                currentIntensity: selectedIntensity,
                timeRemainingInSegment: timeRemainingInSegment,
                currentSongEndingIn: 30, // Assume song ending in 30 seconds
                userPreferences: profileManager.userProfile.musicPreferences,
                recentSongs: [],
                currentDistance: currentDistance * 1000, // Convert to meters
                currentPace: pace,
                isActive: true
            )
            
            print("🎭 Madlib context created, calling ChatGPT...")
            
            // Generate suggestion based on madlib parameters
            let suggestion: MusicSuggestion
            
            if preferLibrarySelection {
                // Force library selection
                suggestion = try await chatGPTService.getRandomLibrarySong(
                    preferences: profileManager.userProfile.musicPreferences,
                    intensity: selectedIntensity
                )
            } else {
                // Use normal generation with madlib context
                suggestion = try await withTimeout(seconds: 30) {
                    try await self.chatGPTService.generateMusicSuggestion(
                        context: madlibContext,
                        userPreferences: self.profileManager.userProfile.musicPreferences
                    )
                }
            }
            
            print("✅ Madlib generation successful: \(suggestion.songTitle) by \(suggestion.artist)")
            testSuggestion = suggestion
            
        } catch {
            print("❌ Madlib generation failed: \(error)")
            self.error = error
        }
    }
    
