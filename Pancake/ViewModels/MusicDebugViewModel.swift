    // Helper function for timeout handling
    private func withTimeout<T>(seconds: TimeInterval, operation: @escaping () async throws -> T) async throws -> T {
        return try await withThrowingTaskGroup(of: T.self) { group in
            group.addTask {
                try await operation()
            }
            
            group.addTask {
                try await Task.sleep(nanoseconds: UInt64(seconds * 1_000_000_000))
                throw TimeoutError()
            }
            
            guard let result = try await group.next() else {
                throw TimeoutError()
            }
            
            group.cancelAll()
            return result
        }
    }
    
    func generateAndPlayTestSuggestion() async {
        print("🎵 Starting full test: ChatGPT + Apple Music")
        
        // 1) Ensure Apple Music authorization
        if !isMusicAuthorized {
            print("🔐 Requesting music authorization...")
            await withCheckedContinuation { continuation in
                profileManager.requestMusicAuthorization()
                DispatchQueue.main.asyncAfter(deadline: .now() + 0.5) {
                    continuation.resume()
                }
            }
            if !isMusicAuthorized {
                print("❌ Music authorization failed")
                self.error = MusicError.notAuthorized
                return
            }
        }
        print("✅ Music authorization confirmed")

        // 2) Ensure there is some content locally
        loadLibraryCount()
        print("📚 Library count: \(libraryCount)")
        if libraryCount == 0 {
            print("⚠️ No local music found, but continuing test...")
            self.error = DebugError.noLocalMusic
            // Continue anyway to exercise the pipeline; playback may still fail
        }

        // 3) Generate suggestion
        print("🤖 Generating ChatGPT suggestion...")
        await testMusicSuggestion()
        
        // Check if ChatGPT generation failed (not just warnings)
        if let error = self.error, error is ChatGPTError {
            print("❌ ChatGPT generation failed: \(error)")
            return
        }

        // 4) Play it (even if there are warnings like noLocalMusic)
        print("🎵 Attempting to play suggestion...")
        await playTestSuggestion()
        print("✅ Full test completed")
    }
    
