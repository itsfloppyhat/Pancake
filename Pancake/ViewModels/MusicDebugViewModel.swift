        } else {
            print("❌ Exact title search found no results")
        }
        
        // Test fuzzy search
        let fuzzyMatches = findFuzzyMatches(in: allSongs, for: suggestion)
        if !fuzzyMatches.isEmpty {
            print("🔍 Fuzzy search found \(fuzzyMatches.count) results:")
            for (index, song) in fuzzyMatches.prefix(5).enumerated() {
                print("   \(index + 1). '\(song.title ?? "Unknown")' by \(song.artist ?? "Unknown")")
