        )
        exactQuery.addFilterPredicate(exactPredicate)
        
        if let exactResults = exactQuery.items, !exactResults.isEmpty {
            print("✅ Exact title search found \(exactResults.count) results:")
            for (index, song) in exactResults.prefix(3).enumerated() {
